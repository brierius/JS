// calcule a raiz de um numero inteiro e depois o valor deste numero elevado ao quadrado
// entradas => numero inteiro
// saida raiz do numero inteiro e o valor do numero elevado ao quadrado

let numero = 10

raiz = Math.sqrt(numero)
quadrado = raiz*raiz

console.log(`A raiz de ${numero} é ${raiz}`)
console.log(`O quadrado de ${raiz} é ${quadrado}`)